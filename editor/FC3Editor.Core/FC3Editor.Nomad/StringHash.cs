		public static uint MakeCRC32(byte[] bytes)
		{
			uint num = 4294967295u;
			for (int i = 0; i < bytes.Length; i++)
			{
				byte b = bytes[i];
				num = (num >> 8 ^ StringHash.CRCtable[(int)((UIntPtr)((num & 255u) ^ (uint)b))]);
			}
			return num ^ 4294967295u;
		}
